# Question: Write a Python program to take first name and last name
# as input and print the full name.

# User se first name input liya ja raha hai
first = input("ENTER YOUR FIRST NAME = ")

# User se last name input liya ja raha hai
last = input("ENTER YOUR LAST NAME = ")

# Dono names ko space ke sath jod kar full name banaya ja raha hai
# Yaha + operator string concatenation ke liye use hua hai
full = first + " " + last

# Full name print kiya ja raha hai
print("THE FULL NAME IS = ", full)